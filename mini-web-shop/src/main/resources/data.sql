DROP TABLE IF EXISTS PROIZVOD;
CREATE TABLE PROIZVOD(
    ID NUMBER PRIMARY KEY AUTO_INCREMENT,
    NAZIV VARCHAR(255),
    OPIS VARCHAR(255),
    CIJENA DECIMAL,
    KOLICINA NUMBER,
    BRAND_ID NUMBER
);

DROP TABLE IF EXISTS BRAND;
CREATE TABLE BRAND(
    ID NUMBER PRIMARY KEY AUTO_INCREMENT,
    NAZIV VARCHAR(255)
);

DROP TABLE IF EXISTS NARUDZBA;
CREATE TABLE NARUDZBA(
    ID NUMBER PRIMARY KEY AUTO_INCREMENT,
    DATUM DATE,
    UKUPNA_CIJENA_BEZ_P DECIMAL,
    UKUPNA_CIJENA_S_P DECIMAL,
    KOD_ZA_POPUST_ID NUMBER,
    NACIN_PLACANJA_ID NUMBER,
    BROJ_KARTICE VARCHAR(255),
    EMAIL VARCHAR(255),
    BROJ_MOBITELA NUMBER(255),
    ADRESA_DOSTAVE VARCHAR(255),
    NAPOMENA VARCHAR(255)
);

DROP TABLE IF EXISTS NARUDZBA_PROIZVODI;
CREATE TABLE NARUDZBA_PROIZVODI(
    ID NUMBER PRIMARY KEY AUTO_INCREMENT,
    NARUDZBA_ID NUMBER,
    PROIZVOD_ID NUMBER
);

DROP TABLE IF EXISTS NACIN_PLACANJA;
CREATE TABLE NACIN_PLACANJA(
    ID NUMBER PRIMARY KEY AUTO_INCREMENT,
    NAZIV VARCHAR(255)
);

DROP TABLE IF EXISTS POPUST_KODOVI;
CREATE TABLE POPUST_KODOVI(
    ID NUMBER PRIMARY KEY AUTO_INCREMENT,
    KOD VARCHAR(255),
    POPUST DECIMAL,
    ISKORISTEN BOOLEAN
);

